public class ZYX {
    public int zz;
    public String yy;

    public ZYX() {
    }

    public ZYX(final int zz, final String yy) {
        this.zz = zz;
        this.yy = yy;
    }

    @Override
    public String toString() {
        return "ZYX{" +
                "zz=" + zz +
                ", yy='" + yy + '\'' +
                '}';
    }
}
